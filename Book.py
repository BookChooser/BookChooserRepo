class Book:

    def __init__(self, title, series, author, isbn10, isbn13, year, page_count, description, cover_url):
        self.title = title
        self.series = series
        self.author = author
        self.isbn10 = isbn10
        self.isbn13 = isbn13
        self.year = year
        self.page_count = page_count
        self.description = description
        self.cover_url = cover_url